class Car {
	constructor(id, model, price, year, type, diesel, volume, km, power, transmission, dors, seats, img) {
		this.id = id;
		this.model = model;
		this.price = price;
		this.year = year;
		this.type = type;
		this.diesel = diesel;
		this.volume = volume;
		this.km = km;
		this.power = power;
		this.transmission = transmission;
		this.dors = dors;
		this.seats = seats;
		this.img = img;
	}
}